SELECT GENDER, AVG(MATH_SCORE), AVG(PHYSICS_SCORE), AVG(CHEMISTRY_SCORE) FROM STUDENT_INFORMATION GROUP BY GENDER;

SELECT TEST_PREPARETION, AVG((MATH_SCORE + PHYSICS_SCORE + CHEMISTRY_SCORE)/3) AS TOTAL_AVG_SCORE FROM STUDENT_INFORMATION GROUP BY TEST_PREPARETION;

SELECT PARENTAL_LEVEL_OF_EDUCATION, AVG((MATH_SCORE + PHYSICS_SCORE + CHEMISTRY_SCORE)/3) AS TOTAL_AVG_SCORE FROM STUDENT_INFORMATION GROUP BY PARENTAL_LEVEL_OF_EDUCATION ORDER BY TOTAL_AVG_SCORE DESC;
